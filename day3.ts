let visitedTreeCount = 0;

while (currentlyVisitedRow + SLOPE_DOWN_LENGTH < rows.length) {
    currentlyVisitedColumn = currentlyVisitedColumn + SLOPE_RIGHT_LENGTH;
    currentlyVisitedRow = currentlyVisitedRow + SLOPE_DOWN_LENGTH;

    if (currentlyVisitedColumn >= numberOfColumns) {
        currentlyVisitedColumn = currentlyVisitedColumn - numberOfColumns;
    }

    const nextCharVisited = rows[currentlyVisitedRow].charAt(currentlyVisitedColumn);
    const visitingTree = nextCharVisited === "#";

    if (visitingTree) {
        visitedTreeCount++;
    }
}

